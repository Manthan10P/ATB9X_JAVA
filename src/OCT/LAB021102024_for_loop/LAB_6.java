package OCT.LAB021102024_for_loop;

public class LAB_6 {
    public static void main(String[] args) {
        int i = 0;
        int j = 0;


        for (i = 0; i <= 5 ; i++) {
            for (j = 0; j <= i; j++) {
                System.out.print("*");

            }
            System.out.println("#");

        }

    }
}
